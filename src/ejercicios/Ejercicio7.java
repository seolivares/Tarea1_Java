package ejercicios;

public class Ejercicio7 {
    public static void main(String[] args) {
        /*
        7. Área de un triángulo
•	    Descripción: Dadas la base b = 5 y la altura h = 8 de un triángulo, calcula su área usando la fórmula:
        Area = 1/2 * base * altura
•	    Variables: b = 5, h = 8
     */

        double b = 5, h = 8;
        double area = 1.0/2 * b * h;

        System.out.println("El área de un triángulo de base = " + b + " y altura = " + h + " es: " + area);
    }
}
